
# кортежи. неизменяемые

t1 = (1,2,3,4,5)

t2 = 1,2,3,4 # это тоже кортеж

print('t1 %s' % (t1,))
print('t2 %s' % (t2,))

# объединение кортежей

t1 = (1,2)
t2 = (4,)

t3 = t1 + t2

print('%s + %s = %s' % (t1, t2, t3))

# кол-во вхождений элемента

t = 1,2,4,3,3,3

print('3 in %s = %s' % (t, t.count(3),))

# получение индекса элемента

print(t.index(2))

# сортировка кортежа 

t = (4,5,6,1,2)

print(t)

print(sorted(t))

